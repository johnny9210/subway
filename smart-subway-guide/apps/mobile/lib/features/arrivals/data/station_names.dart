  /// 예: "군자역 5호선" → "군자(능동)"
  static String normalize(String stationName) {
    // 1. "역" 제거, 호선 정보 제거
    String baseName = stationName
        .replaceAll(RegExp(r'\s+\d+호선$'), '')
        .replaceAll(RegExp(r'\s+(신분당선|경의중앙선|공항철도|경춘선|수인분당선|경강선|우이신설선|서해선|신림선|GTX-A)$'), '')
        .replaceAll('역', '')
        .trim();

    // 2. 매핑 테이블에서 찾기 (괄호 포함된 정식 이름)
    final mapped = _displayToApiName[baseName];
    if (mapped != null) {
      return mapped;
    }

    // 3. 매핑이 없으면 기본 이름 반환 (대부분의 역은 그대로 사용)
    return baseName;
  }

